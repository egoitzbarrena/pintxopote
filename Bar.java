// nombre , num bar

public class Bar {

private String nombreBar;
private Integer numBar;
private String telefono;
private String localizacion;


	public String getNombreBar () {

		return nombreBar;
	}
	public void setNombreBar (String nb){

		nombreBar = nb;
	}

	public Integer getNumBar () {

		return numBar;
	}
	public void setNumBar (Integer nb2){

		numBar = nb2;
	}

	public String getTelefono () {

		return telefono;
	}
	public void setTelefono (String tel){

		telefono = tel;
	}

	public String getLocalizacion () {

		return localizacion;
	}
	public void setLocalizacion (String loc){

		localizacion = loc;
	}

	
	


}


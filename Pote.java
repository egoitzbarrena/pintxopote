// nombre,  num pote, precio

public class Pote {

	private String nombrePote;
	private Integer numPote;
	private Double precioPote;

	public String getNombrePote() {

		return nombrePote;
	} 

	public void setNombrePote (String nomPote){

		nombrePote = nomPote ;
	}

	public Integer getNumPote() {

		return numPote;
	} 

	public void setNumPote (Integer numPote){

		numPote = numPote;
	}

	public Double getPrecioPote () {

		return precioPote;
	} 

	public void setPrecioPote (Double prePote){

		precioPote = prePote ;
	}


}

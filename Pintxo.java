// nombre , num pintxo, precio

public class Pintxo {

	private String nombrePintxo;
	private Integer numPintxo;
	private Double precioPintxo;

	public String getNombrePintxo() {

		return nombrePintxo;
	} 

	public void setNombrePintxo (String nomPin){

		nombrePintxo = nomPin ;
	}

	public Integer getNumPintxo() {

		return numPintxo;
	} 

	public void setNumPintxo (Integer numPin){

		numPintxo = numPin;
	}

	public Double getPrecioPintxo () {

		return precioPintxo;
	} 

	public void setPrecioPintxo (Double prePin){

		precioPintxo = prePin ;
	}


}